# Update Candidate Status
# ==========================================================

def update_candidate_status(candidate_id, status):
    """
    Update the recruitment status of a candidate.

    Parameters
    ----------
    candidate_id : int
        Unique candidate ID.

    status : str
        New recruitment status.

        Example:
            Pending
            Shortlisted
            Rejected
            Hired

    Returns
    -------
    dict
        Status message indicating whether the update
        was successful.
    """

    # ------------------------------------------------------
    # Create Database Connection
    # ------------------------------------------------------

    connection = get_connection()

    cursor = connection.cursor()

    # ------------------------------------------------------
    # Update Candidate Status
    # ------------------------------------------------------

    cursor.execute("""

        UPDATE candidates

        SET status = ?

        WHERE id = ?

    """, (

        status,

        candidate_id

    ))

    # ------------------------------------------------------
    # Save Changes
    # ------------------------------------------------------

    connection.commit()

    # ------------------------------------------------------
    # Check Whether Record Exists
    # ------------------------------------------------------

    if cursor.rowcount == 0:

        connection.close()

        return {

            "Status": "Failed",

            "Message": "Candidate not found."

        }

    # ------------------------------------------------------
    # Close Connection
    # ------------------------------------------------------

    connection.close()

    # ------------------------------------------------------
    # Return Success
    # ------------------------------------------------------

    return {

        "Status": "Success",

        "Message": "Candidate status updated successfully."

    }

